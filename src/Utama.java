public class Utama {

    private String nama;
    private String peran;
    
    public Utama(String nama, String peran){
        this.nama=nama;
        this.peran=peran;
    }

    public String getNama() {
        return nama;
    }

    public String getPeran() {
        return peran;
    }
    
}
